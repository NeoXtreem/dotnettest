        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, MoontronDeployment moontronDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<MoontronDeployment>().SendRequestAndWaitForReceiptAsync(moontronDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, MoontronDeployment moontronDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<MoontronDeployment>().SendRequestAsync(moontronDeployment);
        }

        public static async Task<MoontronService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, MoontronDeployment moontronDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, moontronDeployment, cancellationTokenSource);
            return new MoontronService(web3, receipt.ContractAddress);
        }

